// Cross Product:

// The cross product, also known as the vector product, is a binary operation on two vectors in three-dimensional space. The result is a vector that is perpendicular to both input vectors. The magnitude of the cross product is equal to the area of the parallelogram formed by the two vectors. 

// Use Cases in Graphics:

// 1. Surface Normal: The cross product is commonly used to find the normal vector to a surface defined by two vectors. Normals are crucial in lighting calculations for realistic rendering.

// 2. Rotation: Cross product is used in rotation calculations, such as finding the axis of rotation and determining the direction of rotation.

// 3. Collision Detection: In 3D graphics, the cross product can be used in collision detection algorithms, helping to determine the orientation of objects.


